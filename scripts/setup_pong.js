async function main() {
    console.log("Connecting to local EVM provider...");
    const provider = new ethers.JsonRpcProvider(PROVIDER_URL);
    const signers = await provider.listAccounts();
    if (signers.length === 0) {
        throw new Error("No accounts available on EVM node.");
    }

    const config = JSON.parse(fs.readFileSync(CONFIG_PATH, "utf8"));
    const diskSystemAddress = config.networks.localhost.diskSystemAddress;

    console.log("\nUploading new interactive 'pong' cartridge...");
    const signer = signers[0];
    const header = "W0:pong\x00";
    const headerBytes = ethers.toUtf8Bytes(header);
    
    let payload = new Uint8Array(pongProgram);
    const fullCmdBytes = ethers.concat([headerBytes, payload]);

    const executeSelector = "0x9812a4df";
    const offsetSlot = ethers.zeroPadValue(ethers.toBeHex(32), 32);
    const lengthSlot = ethers.zeroPadValue(ethers.toBeHex(fullCmdBytes.length), 32);
    const paddedBytes = ethers.concat([fullCmdBytes, ethers.zeroPadValue("0x", (32 - (fullCmdBytes.length % 32)) % 32)]);
    const calldata = ethers.concat([executeSelector, offsetSlot, lengthSlot, paddedBytes]);

    const tx = await signer.sendTransaction({
        to: diskSystemAddress,
        data: calldata
    });
    await tx.wait();
    console.log("Successfully uploaded 'pong' to DFS.");
}

main().catch(err => {
    console.error(err);
    process.exit(1);
});
